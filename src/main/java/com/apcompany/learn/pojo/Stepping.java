package com.apcompany.learn.pojo;

public class Stepping {
	
	private int id;
	private int questionId;
	private int step;
	private String procedurequestiong;
	private String proceduranswer;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getQuestionId() {
		return questionId;
	}
	public void setQuestionId(int questionId) {
		this.questionId = questionId;
	}
	public int getStep() {
		return step;
	}
	public void setStep(int step) {
		this.step = step;
	}
	public String getProcedurequestiong() {
		return procedurequestiong;
	}
	public void setProcedurequestiong(String procedurequestiong) {
		this.procedurequestiong = procedurequestiong;
	}
	public String getProceduranswer() {
		return proceduranswer;
	}
	public void setProceduranswer(String proceduranswer) {
		this.proceduranswer = proceduranswer;
	}

}
